ll solve(ll L, ll R){
	if(L>=R){
		return 0;
	}
	if(dp[L][R]!=-1){
		return dp[L][R];
	}
	ll ans=A[R]-A[L];
	ans+=min(solve(L,R-1),solve(L+1,R));
	return dp[L][R]=ans;
}

